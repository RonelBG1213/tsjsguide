//javascript control structures
// If-Else
let num = 10;
if (num > 0) {
  console.log("Positive number");
}
else if (num < 0) {
  console.log("Negative number");
} else {
  console.log("Zero");
} 
// Switch 
let day = 3;
switch (day) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  default:
    console.log("Another day");
}